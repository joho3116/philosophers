#include "libft.h"

void	ft_bzero(void *dest, size_t n)
{
	unsigned char	*buffer;
	size_t			i;

	buffer = dest;
	i = 0;
	while (i < n)
	{
		buffer[i] = 0;
		i++;
	}
}
